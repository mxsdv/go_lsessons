// todo: Решить линейное уравнение A·x + B = 0, заданное своими коэффициентами A и B (коэффициент A не равен 0).
// Примечание: коэффициенты получаем через консольный ввод.

package main

import (
	"fmt"
)

func main() {
	var A, B float64
	fmt.Print("Введите коэффициент A (не равен 0): ")
	fmt.Scan(&A)
	if A == 0 {
		fmt.Println("Ошибка: коэффициент A не должен быть равен 0!")
		return
	}
	fmt.Print("Введите коэффициент B: ")
	fmt.Scan(&B)
	x := -B / A
	fmt.Printf("Решение уравнения: x = %.2f\n", x)
}
